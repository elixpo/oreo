_FALLBACK = (0x7F, 0x41, 0x41, 0x41, 0x7F)   # filled box


def char_width(scale=1):
    """Width (px) of one glyph at given scale, including kerning."""
    return (GLYPH_W + GLYPH_KERN) * scale


def height(scale=1):
    return GLYPH_H * scale


def measure(text, scale=1):
    if not text:
        return 0
    return (len(text) * (GLYPH_W + GLYPH_KERN) - GLYPH_KERN) * scale


def char(d, ch, x, y, color, scale=1):
    """Draw a single glyph at (x, y)."""
    cols = _FONT.get(ch, _FALLBACK)
    for cx, col in enumerate(cols):
        for ry in range(GLYPH_H):
            if col & (1 << ry):
                d.rect(x + cx * scale, y + ry * scale, scale, scale, color, fill=True)


def text(d, s, x, y, color=api.WHITE, scale=1, kern=GLYPH_KERN):
    """Draw a string at (x, y). Returns x after the last glyph."""
    cx = x
    for ch in s:
        char(d, ch, cx, y, color, scale)
        cx += (GLYPH_W + kern) * scale
    return cx


def text_center(d, s, cx, y, color=api.WHITE, scale=1):
    """Draw `s` centred horizontally around cx."""
    w = measure(s, scale)
    text(d, s, cx - w // 2, y, color, scale)
